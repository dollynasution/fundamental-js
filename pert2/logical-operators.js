// logical operators

const statement1 = 5 > 3 //true
const statement2 = 40 < 3 //false

// OR operator
console.log(statement1 || statement2)

// AND operator
console.log(statement1 && statement2)

// NOT operator (negation of statement)
console.log(!statement1)
console.log(!statement2)

// angka selain 0 = true
// 0 = false
console.log(!0)