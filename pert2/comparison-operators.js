// comparison operators menghasilkan boolean
// comparison operators tidak berlaku untuk tipe data object

// == perbandingan tanpa melihat tipe data (equal operator)
console.log(5==5) //true
console.log(5=='5') //true

// === perbandingan mempertimbangkan tipe data (strict equal operator)
console.log(5 === 5) //true
console.log(5 === '5') //false - lebih disarankan menggunakan ini untuk boolean

// > lebih dari
console.log(5 > 3) //true

// < kurang dari
console.log(5 > 3) //true

// >= lebih dari sama dengan
console.log(5 >= 5) //true

// <= lebih dari sama dengan
console.log(5 <= 3) //false

// > lebih dari